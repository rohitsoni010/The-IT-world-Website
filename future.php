<p>
	<br>
The World Health Organization estimates that 1.25 million people die from car-related injuries per year. Half of the deaths are pedestrians, bicyclists, and motorcyclists hit by cars. Cars are the leading cause of death for people ages 15-29 years old.
</p><center>
<img src="images/self-driving-cars.png" width="70%"></center>
<p>
Just as cars reshaped the world in the 20th century, so will self-driving cars in the 21st century. In most cities, between 20-30% of usable space is taken up by parking spaces, and most cars are parked about 95% of the time . Self-driving cars will be in almost continuous use (most likely hailed from a smartphone app), thereby dramatically reducing the need for parking. Cars will communicate with one another to avoid accidents and traffic jams, and riders will be able to spend commuting time on other activities like work, education, and socializing.</p>

<h4><li>Clean Energy</h4></li>
<p>
Attempts to fight climate change by reducing the demand for energy haven't worked . Fortunately, scientists, engineers, and entrepreneurs have been working hard on the supply side to make clean energy convenient and cost-effective.
</p>
<p>
Due to steady technological and manufacturing advances, the price of solar cells has dropped 99.5% since 1977. Solar will soon be more cost efficient than fossil fuels. The cost of wind energy has also dropped to an all-time low, and in the last decade represented about a third of newly installed US energy capacity.
</p><p>
Forward thinking organizations are taking advantage of this. For example, in India there is an initiative to convert airports to self-sustaining clean energy.
</p><p>
Tesla is making high-performance, affordable electric cars, and installing electric charging stations worldwide.There are hopeful signs that clean energy could soon be reaching a tipping point. For example, in Japan, there are now more electric charging stations than gas stations. And Germany produces so much renewable energy, it sometimes produces even more than it can use.
<center><img src="images/tesla.jpg"></center></p>

<h4><li>Virtual and Augmented Reality</li></h4>
<p>
Computer processors only recently became fast enough to power comfortable and convincing virtual and augmented reality experiences. Companies like Facebook, Google, Apple, and Microsoft are investing billions of dollars to make VR and AR more immersive, comfortable, and affordable.
</p><p>
People sometimes think VR and AR will be used only for gaming, but over time they will be used for all sorts of activities. For example, we'll use them to manipulate 3-D objects.</p>
<center><img src="images/virtual.gif"></center><p>
To meet with friends and colleagues from around the world. And even for medical applications, like treating phobias or helping rehabilitate paralysis victims.</p><p>
VR and AR have been dreamed about by science fiction fans for decades. In the next few years, they'll finally become a mainstream reality.</p>


<h4><li>Drones and Flying Cars</li></h4>
<p>
"Roads? Where we're going we don't need... roads."?-?Dr. Emmet Brown</p>
<center>
<img src="images/drone.jpg"></center>
<p>
GPS started out as a military technology but is now used to hail taxis, get mapping directions, and hunt Pokemon. Likewise, drones started out as a military technology, but are increasingly being used for a wide range of consumer and commercial applications.</p>
<p>
<ul><li class="s">Drones are being used to inspect critical infrastructure like bridges and power lines, to survey areas struck by natural disasters, and many other creative uses like fighting animal poaching.

<li class="s">Amazon and Google are building drones to deliver household items.

<li class="s">Amazon Drone Smaller Reuters/Gus Ruelas / Amazon
<li class="s">The startup Zipline uses drones to deliver medical supplies to remote villages that can't be accessed by roads.

<li class="s">There is also a new wave of startups working on flying cars (including two funded by the cofounder of Google, Larry Page).

<li class="s">Flying cars use the same advanced technology used in drones but are large enough to carry people. Due to advances in materials, batteries, and software, flying cars will be significantly more affordable and convenient than today's planes and helicopters.</ul>


<h4><li>Artificial Intelligence</li></h4>
<p>	It may be a hundred years before a computer beats humans at Go?-?maybe even longer.?-?New York Times, 1997
	Artificial intelligence has made rapid advances in the last decade, due to new algorithms and massive increases in data collection and computing power.
</p><p>
	AI can be applied to almost any field. For example, in photography an AI technique called artistic style transfer transforms photographs into the style of a given painter.
</p><ul>
	<li class="s">
	Google built an AI system that controls its datacenter power systems, saving hundreds of millions of dollars in energy costs.

		<li class="s">
	The broad promise of AI is to liberate people from repetitive mental tasks the same way the industrial revolution liberated people from repetitive physical tasks.

		<li class="s">
	Some people worry that AI will destroy jobs. History has shown that while new technology does indeed eliminate jobs, it also creates new and better jobs to replace them. For example, with advent of the personal computer, the number of typographer jobs dropped, but the increase in graphic designer jobs more than made up for it.

		<li class="s">
	Master of Go Board Game Is Walloped by Google Computer Program"?-?New York Times, 2016
	It is much easier to imagine jobs that will go away than new jobs that will be created. Today millions of people work as app developers, ride-sharing drivers, drone operators, and social media marketers- jobs that didn't exist and would have been difficult to even imagine ten years ago.
</ul></p>
